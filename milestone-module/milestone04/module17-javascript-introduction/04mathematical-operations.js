
// Simple Mathematical Operations in JavaScript

// Addition, Subtraction, Multiplication, Division

var onionPrice = 40;
var eggPrice = 50;
var eggQuantity = 10;
var orangePrice = 10;
moneyAvailable = 500;

var totalPrice = onionPrice + eggPrice; 
var priceDifference = onionPrice - eggPrice;
var eggPriceMultiplicaiton = eggPrice * eggQuantity;
var orangeQuantity = moneyAvailable / orangePrice;

console.log(totalPrice); // 90
console.log(priceDifference); // -10
console.log(eggPriceMultiplicaiton); // 500
console.log(orangeQuantity); // 50


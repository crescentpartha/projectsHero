
// (Advanced) Mathematical Operation Shorthand

var price1 = 31;
var price2 = 10; 

var sum = price1 + price2;
var difference = price1 - price2;
var multiplicaiton = price1 * price2;
var division = price1 / price2;

console.log(sum); // 41
console.log(difference); // 21
console.log(multiplicaiton); // 310
console.log(division); // 3.1

var doublePrice = price2 * 2;
var newPrice = price1 + 10;

console.log(doublePrice); // 20
console.log(newPrice); // 41

// price1 = price1 + 10;
price1 += 10;
console.log(price1); // 41

var age = 21;
console.log(age); // 21

// age = age + 1;
// age += 1;
age++;
console.log(age); // 22


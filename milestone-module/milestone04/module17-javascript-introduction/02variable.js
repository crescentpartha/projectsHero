
// What is variable, five things you need to declare a variable

// Variable Declaration:
//     var variable_name = variable_value;

var price = 50;
var age = 21; 
var temperature = 37;

console.log(price); // 50
console.log(age); // 21
console.log(temperature); // 37

// Numeric type variable 
var classNumber = 7;
console.log(classNumber); // 7

// String type variable
var name = "Crescent Partha";
var university = 'SUST';

console.log(name); // Crescent Partha
console.log(university); // SUST

// Boolean type variable 
var pass = true;
var subscribed = false;

console.log(pass); // true
console.log(subscribed); // false



// Compare variables and Comparison operator

console.log(5 > 6); // false
console.log(5 < 6); // true
console.log(5 == 6); // false
console.log(6 == 6); // true
console.log(6 != 6); // false

var myLove = 99;
var yourLove = 100;
console.log(myLove > yourLove); // false
console.log(myLove == yourLove); // false
console.log(myLove < yourLove); // true
console.log(myLove != yourLove); // true

var studyGood = true;
var goodPerson = true;
var isBanker = false;
var goodResult = true;

console.log(studyGood && goodPerson); // true
console.log(studyGood && isBanker); // false
console.log(isBanker || goodResult); // true
console.log(goodPerson || goodResult); // true

